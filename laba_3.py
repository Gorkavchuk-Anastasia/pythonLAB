# Пусть пользователь введёт скорость, нашей
# задачей будет проверить, превысил ли он скоростной режим, если скорость будет больше
# 60, то выведем сообщение о нарушении, а если
# нет, то сообщение о то, что он прекрасно соблюдает скоростной режим.
var1 = int(input("введите скорость "))
if var1 > 60:
    print("вы превысили скорость")
else:
    print("вы прекрасно соблюдаете скоростной режим") 